    omlaag gegaan. Dit komt voornamelijk door de reductie van vervoer, wat zorgt
    voor het meeste NO<sub>2</sub> uitstoot. Wanneer al het vervoer dus geen NO<sub
      >2</sub
    >
    zal uitstoten, bijvoorbeeld door elektrische auto's en andere duurzame
    oplossingen, zou de uitstoot met 50% kunnen verminderen.
    <br />
    Dit werkt voor vrijwel alle steden met een hoge populatie, alleen het werkt niet
    bij alle steden even goed. Zo is er in Milaan een kleinere reductie van NO<sub
      >2</sub
    > te zien. Dit komt door de industrie van Milaan. Deze is erg groot en is niet
    op stop gezet, wat dus bleef zorgen voor veel uitstoot.
